package openapiart_test

import (
	"testing"

	openapiart "github.com/open-traffic-generator/openapiart/pkg"
	"github.com/stretchr/testify/assert"
)

func TestConfigGeneratedValidate(t *testing.T) {
	config := openapiart.NewPrefixConfig()

	v1 := config.RequiredObject()
	_ = v1.Validate()
	v2 := config.OptionalObject()
	_ = v2.Validate()
	v3 := config.E()
	_ = v3.Validate()
	v4 := config.F()
	_ = v4.Validate()
	v5 := config.G().Add()
	_ = v5.Validate()
	v6 := config.J().Add()
	v7 := v6.JA()
	_ = v7.Validate()
	v8 := v6.JB()
	_ = v8.Validate()
	_ = v6.Validate()
	v9 := config.K()
	v10 := v9.EObject()
	_ = v10.Validate()
	v11 := v9.FObject()
	_ = v11.Validate()
	_ = v9.Validate()
	v12 := config.L()
	v12.SetInteger(9).SetMac("00:00:00:00:00:0a::0b").SetIpv4("1.1.1.1.1").SetIpv6("2000::1::1").SetHex("0x12JKL")
	_, err := v12.Marshaller().ToProto()
	assert.NotNil(t, err)
	v13 := config.Level()
	v14 := v13.L1P1()
	v15 := v14.L2P1()
	_ = v15.Validate()
	_ = v14.Validate()
	_ = v13.Validate()
	v16 := config.Mandatory()
	_ = v16.Validate()
	v17 := config.Ipv4Pattern()
	v18 := v17.Ipv4()
	v18.SetValue("1.1.1.1.1").SetValues([]string{"1.1.1.1.1"})
	v19 := v18.Increment()
	v19.SetStart("1.1.1.1.1").SetStep("1.1.1.1.1")
	_, err = v19.Marshaller().ToProto()
	assert.NotNil(t, err)
	v20 := v18.Decrement()
	v20.SetStart("1.1.1.1.1").SetStep("1.1.1.1.1")
	_, err = v20.Marshaller().ToProto()
	assert.NotNil(t, err)
	_, err = v18.Marshaller().ToProto()
	assert.NotNil(t, err)
	_ = v17.Validate()
	v21 := config.Ipv6Pattern()
	v22 := v21.Ipv6()
	v22.SetValue("2000::1::1").SetValues([]string{"2000::1::2"})
	v23 := v22.Increment()
	v23.SetStart("2000::1::1").SetStep("2000::1::1")
	_, err = v23.Marshaller().ToProto()
	assert.NotNil(t, err)
	v24 := v22.Decrement()
	v24.SetStart("2000::1::1").SetStep("2000::1::1")
	_, err = v24.Marshaller().ToProto()
	assert.NotNil(t, err)
	_, err = v22.Marshaller().ToProto()
	assert.NotNil(t, err)
	_ = v21.Validate()
	v25 := config.MacPattern()
	v26 := v25.Mac()
	v26.SetValue("00:00:00:00:00:0a::0b").SetValues([]string{"00:00:00:00:00:0a::b"})
	v27 := v26.Increment()
	v27.SetStart("00:00:00:00:00:0a::0b").SetStep("00:00:00:00:00:0a::0b")
	_, err = v27.Marshaller().ToProto()
	assert.NotNil(t, err)
	v28 := v26.Decrement()
	v28.SetStart("00:00:00:00:00:0a::0b").SetStep("00:00:00:00:00:0a::0b")
	_, err = v28.Marshaller().ToProto()
	assert.NotNil(t, err)
	_, err = v26.Marshaller().ToProto()
	assert.NotNil(t, err)
	_ = v25.Validate()
	v29 := config.IntegerPattern()
	v30 := v29.Integer()
	v30.SetValue(1).SetValues([]uint32{1})
	v31 := v30.Increment()
	v31.SetStart(500).SetStep(500)
	_, err = v31.Marshaller().ToProto()
	assert.NotNil(t, err)
	v32 := v30.Decrement()
	v32.SetStart(500).SetStep(500)
	_, err = v32.Marshaller().ToProto()
	assert.NotNil(t, err)
	_, err = v30.Marshaller().ToProto()
	assert.NotNil(t, err)
	_ = v29.Validate()
	v33 := config.ChecksumPattern()
	v34 := v33.Checksum()
	v34.SetCustom(500)
	_, err = v34.Marshaller().ToProto()
	assert.NotNil(t, err)
	_ = v33.Validate()
	v35 := config.MObject()
	v35.SetInteger(9).SetMac("00:00:00:00:00:0a::0b").SetIpv4("1.1.1.1.1").SetIpv6("2000::1::1").SetHex("0x12JKL")
	_, err = v35.Marshaller().ToProto()
	assert.NotNil(t, err)
	v36 := config.HeaderChecksum()
	v36.SetCustom(12345678)
	_, err = v36.Marshaller().ToProto()
	assert.NotNil(t, err)

}
