package openapiart_test

import (
	"testing"

	openapiart "github.com/open-traffic-generator/openapiart/pkg"
	"github.com/stretchr/testify/assert"
)

func TestConfigGeneratedValidate(t *testing.T) {
	api := openapiart.NewApi()
	config := api.NewPrefixConfig()

	v1 := config.RequiredObject()
	v1.Validate()
	v2 := config.OptionalObject()
	v2.Validate()
	v3 := config.E()
	v3.Validate()
	v4 := config.F()
	v4.Validate()
	v5 := config.G().Add()
	v5.Validate()
	v6 := config.J().Add()
	v7 := v6.JA()
	v7.Validate()
	v8 := v6.JB()
	v8.Validate()
	v6.Validate()
	v9 := config.K()
	v10 := v9.EObject()
	v10.Validate()
	v11 := v9.FObject()
	v11.Validate()
	v9.Validate()
	v12 := config.L()
	v12.SetInteger(9).SetMac("00:00:00:00:00:0a::0b").SetIpv4("1.1.1.1.1").SetIpv6("2000::1::1").SetHex("0x12JKL")
	assert.NotNil(t, v12.Validate())
	v13 := config.Level()
	v14 := v13.L1P1()
	v15 := v14.L2P1()
	v15.Validate()
	v14.Validate()
	v13.Validate()
	v16 := config.Mandatory()
	v16.Validate()
	v17 := config.Ipv4Pattern()
	v18 := v17.Ipv4()
	v18.SetValue("1.1.1.1.1").SetValues([]string{"1.1.1.1.1"})
	v19 := v18.Increment()
	v19.SetStart("1.1.1.1.1").SetStep("1.1.1.1.1")
	assert.NotNil(t, v19.Validate())
	v20 := v18.Decrement()
	v20.SetStart("1.1.1.1.1").SetStep("1.1.1.1.1")
	assert.NotNil(t, v20.Validate())
	assert.NotNil(t, v18.Validate())
	v17.Validate()
	v21 := config.Ipv6Pattern()
	v22 := v21.Ipv6()
	v22.SetValue("2000::1::1").SetValues([]string{"2000::1::2"})
	v23 := v22.Increment()
	v23.SetStart("2000::1::1").SetStep("2000::1::1")
	assert.NotNil(t, v23.Validate())
	v24 := v22.Decrement()
	v24.SetStart("2000::1::1").SetStep("2000::1::1")
	assert.NotNil(t, v24.Validate())
	assert.NotNil(t, v22.Validate())
	v21.Validate()
	v25 := config.MacPattern()
	v26 := v25.Mac()
	v26.SetValue("00:00:00:00:00:0a::0b").SetValues([]string{"00:00:00:00:00:0a::b"})
	v27 := v26.Increment()
	v27.SetStart("00:00:00:00:00:0a::0b").SetStep("00:00:00:00:00:0a::0b")
	assert.NotNil(t, v27.Validate())
	v28 := v26.Decrement()
	v28.SetStart("00:00:00:00:00:0a::0b").SetStep("00:00:00:00:00:0a::0b")
	assert.NotNil(t, v28.Validate())
	assert.NotNil(t, v26.Validate())
	v25.Validate()
	v29 := config.IntegerPattern()
	v30 := v29.Integer()
	v30.SetValue(-1).SetValues([]int32{-1})
	v31 := v30.Increment()
	v31.SetStart(-1).SetStep(-1)
	assert.NotNil(t, v31.Validate())
	v32 := v30.Decrement()
	v32.SetStart(-1).SetStep(-1)
	assert.NotNil(t, v32.Validate())
	assert.NotNil(t, v30.Validate())
	v29.Validate()
	v33 := config.ChecksumPattern()
	v34 := v33.Checksum()
	v34.SetCustom(-1)
	assert.NotNil(t, v34.Validate())
	v33.Validate()
	v35 := config.MObject()
	v35.SetInteger(9).SetMac("00:00:00:00:00:0a::0b").SetIpv4("1.1.1.1.1").SetIpv6("2000::1::1").SetHex("0x12JKL")
	assert.NotNil(t, v35.Validate())
	v36 := config.HeaderChecksum()
	v36.SetCustom(-1)
	assert.NotNil(t, v36.Validate())

}
